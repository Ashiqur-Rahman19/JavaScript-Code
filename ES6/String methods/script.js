// startsWith(searchString, position) => check a string starts another string
// endsWith (searchString, length) => check a string ends with anothe string 
// includes (searchString, position) => check if a string contains another string
// all these methods are case sensitive



let message = 'Today is Monday Brother';

console.log(message.startsWith('Today'));
console.log(message.endsWith('Brother'));
console.log(message.includes('Monday'));
